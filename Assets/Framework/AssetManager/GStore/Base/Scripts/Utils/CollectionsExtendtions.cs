using System.Collections;
/// <summary>
/// </summary>
public static class CollectionsExtendtions 
{

    public static bool IsNullOrEmpty(this ICollection i )
    {
        return i == null || i.Count == 0;
    }
    
    public static bool IsNotNullNorEmpty(this ICollection i)
    {
        return i != null && i.Count > 0;
    }
	
	    /// <summary>
    /// 判断数组是否为空
    /// </summary>
    /// <typeparam name="T"></typeparam>
    /// <param name="array"></param>
    /// <returns></returns>
    public static bool IsArrayNullOrEmpty<T>(this ICollection  array) 
    {
        return array == null || array.Count <= 0;
    }

}
